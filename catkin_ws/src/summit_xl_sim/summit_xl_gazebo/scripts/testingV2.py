def odometryCb(msg):
	#print("here")
	#print msg.pose.pose
	global x,y
	x = msg.pose.pose.position.x
	y = msg.pose.pose.position.y

def percentageCover():
	global x,y,mapArray,previousCoordinateX,previousCoordinateY

	currentCoordinateX = (x+20) #adding twenty so we can accurately
	currentCoordinateY = (y+20)
	
	currentCoordinateX = round(currentCoordinateX)
	currentCoordinateY = round(currentCoordinateY)
	x = round(x)
	y = round(y)
	
	rospy.loginfo('(X is %s) (Y is %s)',(x+20),(y+20))
	rospy.loginfo('(currentCoordinateX is %s) (currentCoordinateY is %s)',currentCoordinateX,currentCoordinateY)
	
	if currentCoordinateX == previousCoordinateX and currentCoordinateY == previousCoordinateY:
		print("In same coordinate, nothing to do")
	else:
		if mapArray[int(currentCoordinateY)][int(currentCoordinateX)] == -1:
			print("out of bounds!")
			previousCoordinateX = currentCoordinateX
			previousCoordinateY = currentCoordinateY
		else:
			mapArray[int(currentCoordinateY)][int(currentCoordinateX)]=1
			#print(mapArray)
			previousCoordinateX = currentCoordinateX
			previousCoordinateY = currentCoordinateY
	totalCountMissed = sum(x.count(0) for x in mapArray) - 344
	print(totalCountMissed)
	print(sum(x.count(1) for x in mapArray))
	#print(mapArray.count(0))
	rospy.loginfo("Percentage of missed squares: %f", ((float(totalCountMissed)/float(1256))*100))

if __name__ == "__main__":
	rospy.init_node('odometry', anonymous=True) #make node 
	rospy.Subscriber('/robot/robotnik_base_control/odom',Odometry,odometryCb)
	rate = rospy.Rate(20)

	while not rospy.is_shutdown():

		percentageCover()
		rate.sleep()
